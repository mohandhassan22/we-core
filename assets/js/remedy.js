let DATA = [];

      function render(items) {
        const grid = document.getElementById("grid");
        const empty = document.getElementById("empty");
        grid.innerHTML = "";

        if (items.length === 0) {
          empty.style.display = "block";
          return;
        }
        empty.style.display = "none";

        items.forEach((item) => {
          const card = document.createElement("div");
          card.className = "card";
          card.onclick = () => openPanel(item);
          card.innerHTML = `
      <div class="card-top">
        <div class="card-path">${item.full_path}</div>
        <div class="card-title">${item.title}</div>
        <div class="card-desc">${item.desc || "لا يوجد وصف متاح"}</div>
      </div>
      <div class="card-footer">
        SLA: ${item.sla}
      </div>
    `;
          grid.appendChild(card);
        });
      }

      function doSearch() {
        const q = document.getElementById("srch").value.toLowerCase();
        const filtered = DATA.filter(
          (item) =>
            item.title.toLowerCase().includes(q) ||
            item.desc.toLowerCase().includes(q) ||
            item.full_path.toLowerCase().includes(q),
        );
        render(filtered);
      }

      function openPanel(item) {
        document.getElementById("p-title").innerText = item.title;
        document.getElementById("p-path").innerText = item.full_path;
        document.getElementById("p-desc").innerText =
          item.desc || "لا يوجد وصف متاح";
        document.getElementById("p-action").innerText =
          item.action || "لا يوجد إجراء محدد";
        document.getElementById("p-sla").innerText = item.sla;

        document.getElementById("overlay").classList.add("open");
      }

      function closePanel() {
        document.getElementById("overlay").classList.remove("open");
      }

